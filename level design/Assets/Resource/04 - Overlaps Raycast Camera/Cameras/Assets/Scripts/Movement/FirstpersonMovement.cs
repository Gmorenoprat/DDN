using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class FirstpersonMovement : Movement
{
    protected override void Start()
    {
        base.Start();
    }

    public override void Move()
    {
        rb.velocity = (transform.forward * Input.GetAxis("Vertical") * speedForward +
                        transform.right * Input.GetAxis("Horizontal") * speedRight);

        transform.Rotate(new Vector3(0, Input.GetAxis("Mouse X") * rotSpeed, 0));
    }
}
